import math
x = -3.4
print(math.ceil(x))
# Округление до ближайшего большего числа
print(math.fabs(x))
# Выводит модуль число
x1 = 18
print(math.factorial(x1))
# Выводит факториал числа
print(math.pi)
# Выводит число Пи
print(math.e)
# выводит число e
print(math.fmod(x1, x))
# остаток от деления x1 на x
print(math.modf(x1))
# Возвращает дробную и целую часть числа X. Оба числа имеют тот же знак, что и X.